package aula6.metodos;

public class CalcularMetodos {
	
	//somar
	int somar(int a, int b) {
		return a + b;
	}
	int somar(int a, int b, int c) {
		return a + b + c;
	}
	
	//subtração
	int subtracao(int a, int b) {
		return a - b;
	}
	int subtracao(int a, int b, int c) {
		return a - b - c;
	}
	
	//multiplicação
	int multiplicacao(int a, int b) {
		return a * b;
	}
	int multiplicacao(int a, int b, int c) {
		return a * b * c;
	}
	
	//divisão
	int divisao(int a, int b) {
		return a / b;
	}
	int divisao(int a, int b, int c) {
		return a / b / c;
	}
	
}
